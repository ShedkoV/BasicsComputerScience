"""
2. На вход программе подается строка текста – имя человека.
   Напишите программу, которая выводит на экран приветствие в виде слова «Привет» (без кавычек), после которого должна стоять запятая и пробел, а затем введенное имя.

   Примечание 1. Для считывания текста используйте команду input(), для печати текста на экране используйте команду print().
"""
name = input('Введите имя...')
